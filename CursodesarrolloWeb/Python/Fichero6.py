#Diccionarios en python

diccionario = {"clave": "valor", "clave2": "valor2", "clave3": "valor3"}
print(diccionario)

elemento = diccionario["clave"] #Accedo a un valor en especifico.
print(elemento)

colores = {"Rojo": "Red", "Verde": "Green", "Amarillo": "Yelloww"}
print(colores)

