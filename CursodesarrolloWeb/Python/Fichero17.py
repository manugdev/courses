#Todo lo que hacemos en python son objetos. "Type" nos dice 
#que tipo de objeto es.

numero = 5
print (type(numero))

numero2 = 4.8
print(type(numero2))

hola = "hola"
print(type(hola))

lista = [1,2,3,4]
print(type(lista))

